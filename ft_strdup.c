#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*new_str;
	const size_t	len = ft_strlen(s);
	size_t			i;

	new_str = malloc(sizeof(char) * (len + 1));
	if (!new_str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_str[i] = s[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
